package it.dangelo.saj;

/**
 * Constant wrapper class that contains json character/string elements.
 * See <a href="http://www.json.org/">JSON.org</a>
 */
public class Consts {
	/**
 	 * String boundary (double quotes)
	 */
	public static final char STRING_BOUNDARY = '"';
	/**
 	 * Character used to start array (left bracket)
	 */
	public static final char START_ARRAY = '[';
	/**
 	 * Character used to end array (right bracket)
	 */
	public static final char END_ARRAY = ']';
	/**
 	 * Character used to start object (left brace)
	 */
	public static final char START_OBJECT = '{';
	/**
 	 * Character used to end object (right brace)
	 */
	public static final char END_OBJECT = '}';
	/**
 	 * Attribute value separator (colon)
	 */
	public static final char PAIR_SEPARATOR = ':';
	/**
 	 * Attribute (or member) separator (comma)
	 */
	public static final char MEMBER_SEPARATOR = ',';
	/**
 	 * String representation of null value
	 */
	public static final String NULL_VALUE = "null";
	/**
 	 * String representation of true boolean value
	 */
	public static final String BOOLEAN_TRUE_VALUE = "true";
	/**
 	 * String representation of false boolean value
	 */
	public static final String BOOLEAN_FALSE_VALUE = "false";
	
}
